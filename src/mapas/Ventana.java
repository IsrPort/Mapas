    public void iniciarMatriz(int m[][]) {
        for (int f = 0; f < tam; f++) {
            for (int c = 0; c < tam; c++) {
                mapa[f][c] = 0;
            }
        }
    }

    public void construirMatrizBotones(int m[][]) {
        resCad = new String[this.tam][this.tam];
        //strArray[i] = String.valueOf(intArray[i]);
        for (int f = 0; f < tam; f++) {
            for (int c = 0; c < tam; c++) {
                if (m[f][c] == 1) {
                    color = "magenta";
                }
                if (m[f][c] == 2) {
                    color = "cyan";
                }
                if (m[f][c] == 3) {
                    color = "green";
                }
                if (m[f][c] == 4) {
                    color = "orange";
                }
                //resCad[f][c] = String.valueOf(m[f][c]);
                resCad[f][c] = color;
            }
        }
        panel.setLayout(new GridLayout(tam, tam));
        matrizBotones = new JButton[tam][tam];
        for (int f = 0; f < tam; f++) {
            for (int c = 0; c < tam; c++) {
                JButton nuevoBoton = new JButton(resCad[f][c]);
                String colorApintar = resCad[f][c];
                Color colorbg;
                try {
                    Field field = Class.forName("java.awt.Color").getField(colorApintar);
                    colorbg = (Color) field.get(null);
                } catch (Exception e) {
                    colorbg = null; // Not defined
                }
                nuevoBoton.setBackground(colorbg);
                matrizBotones[f][c] = nuevoBoton;
                //matrizBotones[f][c] = new JButton(resCad[f][c]);
                panel.add(matrizBotones[f][c]);
            }
        }
    }

